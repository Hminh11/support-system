package Model;

/**
 *
 * @author ^Zin^
 */
public class StudentAdd {
     private String name;
    private String sex;
    private String status;
    public StudentAdd(){
        
    }

    public StudentAdd(String name, String sex, String status) {
        this.name = name;
        this.sex = sex;
        this.status = status;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
