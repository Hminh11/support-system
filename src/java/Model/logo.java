package Model;

/**
 *
 * @author Admin
 */
public class logo {
    int id;
    School school;
    String image;

    public logo(int id, School school, String image) {
        this.id = id;
        this.school = school;
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public School getSchool() {
        return school;
    }

    public void setSchool(School school) {
        this.school = school;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
    
}
